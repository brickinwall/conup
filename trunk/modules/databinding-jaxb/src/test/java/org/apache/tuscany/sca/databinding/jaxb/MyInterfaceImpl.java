package org.apache.tuscany.sca.databinding.jaxb;

/**
 * @version $Rev: 673544 $ $Date: 2008-07-03 00:26:10 +0100 (Thu, 03 Jul 2008) $
 */
public class MyInterfaceImpl implements MyInterface {
    private String id;

    /**
     * @see org.apache.tuscany.databinding.jaxb.MyInterface#getId()
     */
    public String getId() {
        return id;
    }

    /**
     * @see org.apache.tuscany.databinding.jaxb.MyInterface#setId(java.lang.String)
     */
    public void setId(String id) {
        this.id = id;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((id == null) ? 0 : id.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        final MyInterfaceImpl other = (MyInterfaceImpl)obj;
        if (id == null) {
            if (other.id != null)
                return false;
        } else if (!id.equals(other.id))
            return false;
        return true;
    }

}
